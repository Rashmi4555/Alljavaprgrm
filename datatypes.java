public class datatypes {
    public static void main(String[] args) {
        int age = 30;
        System.out.println("Age (int): " + age);
        float temperature = 36.6f; 
        System.out.println("Temperature (float): " + temperature);
        char grade = 'A';
        System.out.println("Grade (char): " + grade);
        long population = 7800000000L;  
        System.out.println("World Population (long): " + population);
        short distance = 12000;
        System.out.println("Distance (short): " + distance);
        double price = 99.99;
        System.out.println("Price (double): " + price);
        String name = "John Doe";
        System.out.println("Name (String): " + name);
    }
}

